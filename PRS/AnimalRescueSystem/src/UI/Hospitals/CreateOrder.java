    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int selectRowIndex = jTable1.getSelectedRow();
                if(selectRowIndex < 0){
            JOptionPane.showMessageDialog(this, "Please select a row to be updated");
            return;
        }

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        Encounter selectedProfile = (Encounter)model.getValueAt(selectRowIndex,0  );
        Order o = system.getOrderDirectory().addOrder();
        o.setMedicineName(selectedProfile.getPrescriptionName());
        o.setOrderStatus("Pending");
        o.setQuantity(selectedProfile.getQuantity());
        o.setReciever(selectedProfile.getRecipient());
        o.setSender(system.getCurrentUserId());
        o.setOrderID(selectedProfile.getEncounterID());
        
        JOptionPane.showMessageDialog(this, "Order created");
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
         model.setRowCount(0);
        // System.out.println("doctor "+hospitalManagerDirectory);
         for(Encounter pro: system.getEncounterHistory().getEncounterList()){

             Object[] row = new Object[6];
             row[0] = pro;
             row[1] = pro.getEncounterID();
             row[2] = pro.getCaseID();
             row[3] = pro.getQuantity();
             row[4] = pro.getCaseStatus();
             row[5] = pro.getRecipient();
             
             model.addRow(row );
             
         }
    }


