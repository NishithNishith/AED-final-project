    private void populate() {
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for(FundDonation pro: system.getFundDonationDirectory().getFundDonationList()){

            Object[] row = new Object[4];
            row[0] = pro;
            row[1] = pro.getAmount();
            row[2] = pro.getStatus();
            row[3] = pro.getMessage();

            model.addRow(row );

        }
    }
