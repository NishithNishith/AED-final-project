    private void populate() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for(Accountant pro: system.getAccountantDirectory().getAccountant()){

            Object[] row = new Object[4];
            row[0] = pro;
            row[1] = pro.getSalary();
            row[2] = pro.getAge();
            row[3] = pro.getYearsOfExperience();

            model.addRow(row );

        }
    
    }
