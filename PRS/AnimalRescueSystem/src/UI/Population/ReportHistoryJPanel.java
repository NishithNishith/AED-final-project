    private void populate() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        for(Report pro: reportHistory.getReportList()){

            Object[] row = new Object[4];
            row[0] = pro;
            row[1] = pro.getAnimal();
            row[2] = pro.getCondition();
            row[3] = pro.getStatus();

            model.addRow(row );

        }
    }
